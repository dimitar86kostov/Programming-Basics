function repainting(input){

let nylon = Number(input[0]);
let paint = Number(input[1]);
let thinner = Number(input[2]);
let workingHours = Number(input[3]);

let extraPaint = 0.10 * paint;
let totalPaint = paint + extraPaint;

let = totalNylon = nylon + 2;

let totalMaterialSum = (totalNylon * 1.5 + totalPaint * 14.50 + thinner * 5) + 0.40;

let oneHour = 0.30 * totalMaterialSum;

let workers = workingHours * oneHour;

let finalSum = totalMaterialSum + workers;

console.log(finalSum);
    
}

repainting (["10","11","4","8"])

